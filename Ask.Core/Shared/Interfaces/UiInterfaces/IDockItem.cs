namespace Ask.Core.Shared.Interfaces.UiInterfaces
{
  /// <summary>
  /// Контракт элемента док-панели пользовательского интерфейса.
  /// Определяет минимальный набор свойств, необходимых для отображения
  /// и идентификации элемента в докируемой области UI.
  /// </summary>
  public interface IDockItem
  {
    /// <summary>
    /// Заголовок элемента док-панели.
    /// Используется для отображения в интерфейсе и идентификации элемента.
    /// </summary>
    string Title { get; set; }

    /// <summary>
    /// Содержимое элемента док-панели.
    /// Представляет объект, отображаемый внутри докируемой области.
    /// </summary>
    object Content { get; set; }
  }

}
