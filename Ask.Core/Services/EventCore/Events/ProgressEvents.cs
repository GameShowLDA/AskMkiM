using Ask.Core.Shared.Interfaces.EventInterfaces;

namespace Ask.Core.Services.EventCore.Events
{
  /// <summary>
  /// События пользовательского интерфейса, связанные с отображением и скрытием окна прогресса выполнения.
  /// </summary>
  /// <remarks>
  /// Используются для управления отображением модального окна прогресса с блюром на главном окне
  /// при выполнении длительных операций (например, загрузки данных, запуска тестов, экспорта и т.д.).
  /// </remarks>
  public static class ProgressEvents
  {
    /// <summary>
    /// Событие запроса на отображение окна прогресса.
    /// </summary>
    /// <remarks>
    /// Генерируется при старте длительной операции, чтобы показать пользователю индикатор выполнения
    /// и заблокировать взаимодействие с интерфейсом до её завершения.
    /// </remarks>
    public class RequestShowProgress : IEvent
    {
    }

    /// <summary>
    /// Событие запроса на закрытие окна прогресса.
    /// </summary>
    /// <remarks>
    /// Генерируется по завершении длительной операции, чтобы скрыть индикатор выполнения
    /// и вернуть пользователю возможность взаимодействовать с интерфейсом.
    /// </remarks>
    public class RequestCloseProgress : IEvent
    {
    }
  }
}
