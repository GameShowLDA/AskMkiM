using System.ComponentModel;

namespace NewCore.Base.Function.DBC
{
  /// <summary>
  /// Тип проверки цепи самоконтроля.
  /// </summary>
  public enum TypeConnector
  {
    /// <summary>
    /// Полная проверка всех цепей устройства самоконтроля.
    /// Используется для последовательного запуска всех поддерживаемых тестов.
    /// </summary>
    [Description("Полная проверка устройства")]
    FullCheck = 0,

    /// <summary>
    /// Аналого-цифровой преобразователь (АЦП), используется для измерения входного сигнала.
    /// Подключение: разъем XS3.
    /// </summary>
    [Description("Проверка АЦП")]
    ADC = 3,

    /// <summary>
    /// Аналого-цифровой преобразователь (АЦП) с переполюсовкой,
    /// предназначен для измерения сигнала с измененной полярностью.
    /// Подключение: разъем XS3.
    /// </summary>
    [Description("Проверка АЦП с переполюсовкой")]
    ADCReversed = 4,

    /// <summary>
    /// Проверка мультиметра: измерение напряжения и других электрических параметров.
    /// Подключение: разъем XS4.
    /// </summary>
    [Description("Проверка мультиметра")]
    Multimeter = 2,

    /// <summary>
    /// Проверка источника ПИНТ: подача тестовых значений напряжения и тока.
    /// Подключение: разъем XS5.
    /// </summary>
    [Description("Проверка ПИНТ")]
    PINT = 5,

    /// <summary>
    /// Проверка ППУ: испытание цепей пробойной установкой.
    /// Подключение: разъем XS9.
    /// </summary>
    [Description("Проверка ППУ")]
    BreakdownTester = 7,

    /// <summary>
    /// Проверка блокировочного реле: управление защитными цепями.
    /// Подключение: внутреннее реле.
    /// </summary>
    [Description("Проверка блокировочного реле (внутреннее)")]
    BlockingRelay = 1,

    /// <summary>
    /// Проверка шунта: измерение тока по падению напряжения.
    /// Подключение: встроенный элемент.
    /// </summary>
    [Description("Проверка шунта (встроенный элемент)")]
    Shunt = 6,
  }
}
